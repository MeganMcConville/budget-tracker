package com.megansportfolio.budgettracker.surroundingDates;

public class SurroundingDates {

    private Integer previousMonth;

    private Integer previousYear;

    private Integer nextMonth;

    private Integer nextYear;

    public Integer getPreviousMonth() {
        return previousMonth;
    }

    public void setPreviousMonth(Integer previousMonth) {
        this.previousMonth = previousMonth;
    }

    public Integer getPreviousYear() {
        return previousYear;
    }

    public void setPreviousYear(Integer previousYear) {
        this.previousYear = previousYear;
    }

    public Integer getNextMonth() {
        return nextMonth;
    }

    public void setNextMonth(Integer nextMonth) {
        this.nextMonth = nextMonth;
    }

    public Integer getNextYear() {
        return nextYear;
    }

    public void setNextYear(Integer nextYear) {
        this.nextYear = nextYear;
    }
}
